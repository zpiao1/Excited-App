public class CategoryIcon {
    private String mTag;
    private int mImageId;

    public CategoryIcon(String tag, int imageId) {
        mTag = tag;
        mImageId = imageId;
    }

    public String getTag() {
        return mTag;
    }

    public int getImageId() {
        return mImageId;
    }
}
